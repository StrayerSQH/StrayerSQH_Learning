DATA SEGMENT
	STRING1 DB 'Sun '
	LEN1 EQU $-STRING1
	STRING2 DB 'Qianhao!$'
	LEN2 EQU $-STRING2
	NEWSTRING DB 12 DUP(0)
DATA ENDS

STACK1 SEGMENT STACK
	DB 10 DUP(0)
STACK1 ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA, SS:STACK1
BEGIN:
	MOV AX, DATA
	MOV DS, AX
	MOV ES, AX
	CALL STRCAT
	
	
	MOV AH, 4CH
	INT 21H
	
	STRCAT:
		PUSH AX
		PUSH DX
		LEA SI, STRING1
		LEA DI, NEWSTRING
		XOR CX, CX
		MOV CL, LEN1
		LOP1:
			MOV AL, [SI]
			MOV [DI], AL
			INC SI
			INC DI
			LOOP LOP1
		LEA SI, STRING2
		MOV CL, LEN2
		LOP2:
			MOV AL, [SI]
			MOV [DI], AL
			INC SI
			INC DI
			LOOP LOP2
		MOV AH, 09H
		MOV DX, OFFSET NEWSTRING
		INT 21H
		POP DX
		POP AX
		RET 
CODE ENDS
END BEGIN
	

	

